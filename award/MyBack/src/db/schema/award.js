const { Schema } = require("mongoose");
const mongoose = require("mongoose");

const awardSchema = new Schema(
  {
    id: {
      type: String,
      required: true,
    },
    user_id: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const awardModel = mongoose.model("Award", awardSchema);

module.exports = awardModel;
